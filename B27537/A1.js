//Variables are containers that store data values that can change during the execution of a JavaScript program
// Constants are containers that store data values that cannot be changed after they are declared

/*differences between variables and constants
variables
-you can use  var, let, or const keywords to declare them
-their values can be modified after declaration
-they are used for storing dynamic data that may change during program execution
WHILE
constants
-they Can only be declared using the const keyword.
-Their values must be assigned at the time of declaration and cannot be modified later
-they are Used for storing fixed values that should remain unchanged throughout the program*/
